export function collectMockNews(date) {
  const items = [];
  const base = new Date(`${date}T07:00:00+09:00`).getTime();

  for (const [category, rows] of Object.entries(MOCK_SEED)) {
    rows.forEach(([title, description], index) => {
      items.push({
        id: `${date}-mock-${category}-${index}`,
        date,
        rawCategory: category,
        title,
        description,
        sourceName: "샘플 데이터",
        sourceTier: 3,
        sourceUrl: `https://example.com/${category}/${index}`,
        publishedAt: new Date(base - (index + 2) * 3600 * 1000).toISOString()
      });
    });
  }

  return items;
}
