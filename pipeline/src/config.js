export function todayKey(date = new Date()) {
  // 항상 KST 기준 날짜로 발행한다 (CI는 UTC에서 돌기 때문).
  const kst = new Date(date.getTime() + 9 * 60 * 60 * 1000);
  return kst.toISOString().slice(0, 10);
}
